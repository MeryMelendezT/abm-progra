package mypackage1;

public class ClassDep 
{
  String id;
  String name;
  String region;

  public ClassDep()
  {
  }

  public String getId()
  {
    return id;
  }

  public void setId(String newId)
  {
    id = newId;
  }

  public String getName()
  {
    return name;
  }

  public void setName(String newName)
  {
    name = newName;
  }

  public String getRegion()
  {
    return region;
  }

  public void setRegion(String newRegion)
  {
    region = newRegion;
  }



}